public class bitmanipulation{
	public static void main(String[] args) {
		System.out.println("5 & 6 AND Operator: " + (5&6));
		System.out.println("5 | 6 OR Operator: " + (5|6));
		System.out.println("5 ^ 6 XOR Operator: " + (5^6));
		System.out.println("~5 Compliment Operator: " + (~5));
		System.out.println("5<<6 Left Shift Operator: " + (5<<6));
		System.out.println("5>>6 Right Shift Operator: " + (5>>6));
		
	}
}