public class types{
    public static void main(String args[]){
        byte a = 8;
        System.out.println(a);
        int b = 245;
        System.out.println(b);
        float c = 10.5f;
        System.out.println(c);
        char d = 'R';
        System.out.println(d);
        boolean e = true;
        System.out.println(e);
        //long (use for long int)
        //double (use for long float)
        //short (use for short int)
        
        //Type casting
        float number = 45.89f;
        int intCasting = (int)number;
        System.out.println(intCasting);
        char ch = 'a';
        intCasting = (int)ch;
        System.out.println(intCasting);
    }
}